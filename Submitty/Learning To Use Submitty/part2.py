# Get the user input
in_1 = int(input("Enter a value:"))
in_2 = int(input("Enter a second value:"))

# Print the calculated values

# Addition
print(in_1, "+", in_2, "=", in_1 + in_2)

# Subtraction
print(in_1, "-", in_2, "=", in_1 - in_2)

# Multiplication
print(in_1, "*", in_2, "=", in_1 * in_2)

# Integer divison
print(in_1, "//", in_2, "=", in_1 // in_2)

# Division
print(in_1, "/", in_2, "=", in_1 / in_2)

# Modulus
print(in_1, "%", in_2, "=", in_1 % in_2)